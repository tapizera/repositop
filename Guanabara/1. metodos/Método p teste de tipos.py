n = input('algo: ') # Todos vao retornar True se...
print(n.isnumeric()) # todos forem numeros
print(n.islower()) # todos forem minusculos
print(n.isupper()) # todos forem maiusculos
print(n.istitle()) # todos forem titulo
print(n.isalpha()) # todos forem letras (sem números ou símbolos).
print(n.isalnum()) # todos forem letras ou numeros (sem símbolos).
print(n.isdecimal()) # todos forem decimais
print(n.isdigit()) # todos forem digitos (inclui alguns caracteres especiais de outros alfabetos).
print(n.isspace()) # todos forem espacos
print(n.isidentifier()) # a string pode ser usada como nome de variável em Python.
print(n.isprintable()) # todos os caracteres podem ser impressos (não são caracteres de controle).
print(n.isascii()) # todos os caracteres forem ASCII (padrão americano, sem acentos).